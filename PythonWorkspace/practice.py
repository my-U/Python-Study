# \n : 줄바꿈
print("백문이 불여일견 \n백견이 불여일타")

# \" \' : 문장 내에서 따옴표 표시
print("저는 '코린이'입니다.")
print('저는 "코린이"입니다.')
print("저는 \"코린이\"입니다.")
print("저는 \'코린이\'입니다.")

# \\ : 문장 내에서 \ 표시
print("D:\\Git\\my_U\\Python-Study")

# \r : 커서를 문장 맨 앞으로 이동
print("Red Apple\rPine") # 그냥 이동이 아닌 문자수만큼 덮어쓰기 => PineApple

# \b : 백스페이스
print("Redd\bApple") # 앞의 문자 1개를 삭제

# \t : 탭
print("Red\tApple")